def _cron_en() -> dict:
    months = [
        "January (1)",
        "February (2)",
        "March (3)",
        "April (4)",
        "May (5)",
        "June (6)",
        "July (7)",
        "August (8)",
        "September (9)",
        "October (10)",
        "November (11)",
        "December (12)",
    ]
    mon = {"Every month": "*"} | {months[i]: str(i + 1) for i in range(12)}
    return {
        "Minute": {"Every minute": "*"} | {f"{i:02d}": str(i) for i in range(60)},
        "Stunde": {"Every hour": "*"} | {f"{i:02d}": str(i) for i in range(24)},
        "Tag": {"Every day": "*"} | {f"{i:02d}": str(i) for i in range(1, 32)},
        "Monat": mon,
        "Wochentag": {
            "Every day": "*",
            "Monday": "1",
            "Tuesday": "2",
            "Wednesday": "3",
            "Thursday": "4",
            "Friday": "5",
            "Saturday": "6",
            "Sunday": "0",
        },
    }


def cron_mappings_for_lang(lang: str) -> dict:
    return _cron_de() if lang == "de" else _cron_en()


def translate(lang: str, key: str, **kwargs) -> str:
    d = TRANSLATIONS.get(lang) or TRANSLATIONS["de"]
    s = d.get(key)
    if s is None:
        s = TRANSLATIONS["de"].get(key) or TRANSLATIONS["en"].get(key) or key
    if kwargs:
        try:
            return s.format(**kwargs)
        except (KeyError, ValueError):
            return s
    return s


def _merge(*dicts) -> dict:
    o = {}
    for part in dicts:
        o.update(part)
    return o


